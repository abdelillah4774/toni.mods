const modsList = document.getElementById("mods-list");
const searchInput = document.getElementById("searchInput");

function displayMods(modsToDisplay) {
  modsList.innerHTML = "";
  modsToDisplay.forEach((mod, index) => {
    const card = document.createElement("div");
    card.className = "mod-card";
    card.innerHTML = `
      <img src="${mod.image}" alt="${mod.name}" />
      <div class="mod-info">
        <h3>${mod.name}</h3>
        <p>${mod.description}</p>
        <button class="download-btn" onclick="showDetails(${index})">download</button>
      </div>
    `;
    modsList.appendChild(card);
  });
}

let currentMod = null;

function showDetails(index) {
  currentMod = mods[index];
  document.getElementById("main-page").style.display = "none";
  document.getElementById("mod-details").style.display = "block";
  document.getElementById("mod-name").textContent = currentMod.name;
  document.getElementById("mod-image").src = currentMod.image;
  document.getElementById("mod-description").textContent = currentMod.description;
  document.getElementById("mod-details-desc").textContent = currentMod.details;
}

function goToWait() {
  document.getElementById("mod-details").style.display = "none";
  document.getElementById("wait-page").style.display = "block";

  let counter = 5;
  const interval = setInterval(() => {
    counter--;
    document.getElementById("counter").textContent = counter;
    if (counter === 0) {
      clearInterval(interval);
      showFinalDownload();
    }
  }, 1000);
}

function showFinalDownload() {
  document.getElementById("wait-page").style.display = "none";
  document.getElementById("final-download").style.display = "block";
  document.getElementById("final-link").href = currentMod.file;
}

function backToMain() {
  document.getElementById("main-page").style.display = "block";
  document.getElementById("mod-details").style.display = "none";
  document.getElementById("wait-page").style.display = "none";
  document.getElementById("final-download").style.display = "none";
}

searchInput.addEventListener("input", () => {
  const query = searchInput.value.toLowerCase();
  const filtered = mods.filter(mod =>
    mod.name.toLowerCase().includes(query) || mod.description.toLowerCase().includes(query)
  );
  displayMods(filtered);
});

displayMods(mods);
